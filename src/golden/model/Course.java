package golden.model;

public class Course {
    private Integer courseId;

    private String courseNumber;

    private Integer teacherId;

    private String name;

    private String introduction;

    private String outline;

    private String image;

    private Integer currentNumber;

    private Integer maxNumber;

    private Integer weekFlag;

    private Integer dayFlag;

    private Integer beginCourse;

    private Integer endCourse;

    private Integer area;

    private Integer status;

    private String video;

    public Integer getCourseId() {
        return courseId;
    }

    public void setCourseId(Integer courseId) {
        this.courseId = courseId;
    }

    public String getCourseNumber() {
        return courseNumber;
    }

    public void setCourseNumber(String courseNumber) {
        this.courseNumber = courseNumber;
    }

    public Integer getTeacherId() {
        return teacherId;
    }

    public void setTeacherId(Integer teacherId) {
        this.teacherId = teacherId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIntroduction() {
        return introduction;
    }

    public void setIntroduction(String introduction) {
        this.introduction = introduction;
    }

    public String getOutline() {
        return outline;
    }

    public void setOutline(String outline) {
        this.outline = outline;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public Integer getCurrentNumber() {
        return currentNumber;
    }

    public void setCurrentNumber(Integer currentNumber) {
        this.currentNumber = currentNumber;
    }

    public Integer getMaxNumber() {
        return maxNumber;
    }

    public void setMaxNumber(Integer maxNumber) {
        this.maxNumber = maxNumber;
    }

    public Integer getWeekFlag() {
        return weekFlag;
    }

    public void setWeekFlag(Integer weekFlag) {
        this.weekFlag = weekFlag;
    }

    public Integer getDayFlag() {
        return dayFlag;
    }

    public void setDayFlag(Integer dayFlag) {
        this.dayFlag = dayFlag;
    }

    public Integer getBeginCourse() {
        return beginCourse;
    }

    public void setBeginCourse(Integer beginCourse) {
        this.beginCourse = beginCourse;
    }

    public Integer getEndCourse() {
        return endCourse;
    }

    public void setEndCourse(Integer endCourse) {
        this.endCourse = endCourse;
    }

    public Integer getArea() {
        return area;
    }

    public void setArea(Integer area) {
        this.area = area;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getVideo() {
        return video;
    }

    public void setVideo(String video) {
        this.video = video;
    }
}